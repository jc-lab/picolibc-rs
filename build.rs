// Compilation
// ---------------------------------------------------------------------------

fn cc_build(
    picolibc_dir: &Path,
    out_include_dir: &Path,
    arch: &ArchConfig,
    coff: bool,
) -> Result<cc::Build> {
    let mut build = cc::Build::new();

    let compiler = env::var("PICOLIBC_CC")
        .or_else(|_| env::var("CC"))
        .unwrap_or_else(|_| "clang".to_string());
    build.compiler(compiler);

    // Resolve the clang `--target`: an explicit override wins, otherwise remap
    // the Windows-family targets clang can't build picolibc for as-is (see
    // `remap_clang_target`). When neither applies, leave it to cc-rs. A flag we
    // add here comes after cc-rs's own `--target`, and the last one wins.
    let clang_target = env::var("PICOLIBC_CLANG_TARGET")
        .ok()
        .or_else(|| remap_clang_target(&env::var("TARGET").unwrap_or_default()));
    if let Some(t) = clang_target {
        build.flag(format!("--target={t}"));
    }

    build
        .std("c18")
        .flag("-ffreestanding")
        .flag("-nostdlibinc")
        .flag("-fno-builtin")
        .flag("-fno-common")
        .flag("-fno-stack-protector")
        .flag("-U_FORTIFY_SOURCE")
        .flag("-Wno-unused-command-line-argument")
        .flag("-Wno-implicit-int")
        .flag("-Wno-missing-braces")
        .flag("-Wno-return-type")
        .warnings(false);

    // Cross-compiling to `*-windows-msvc` (e.g. from Linux), cc-rs archives with
    // the MSVC librarian `lib.exe`, which isn't present off a Windows/MSVC host.
    // LLVM ships `llvm-lib`, a drop-in replacement; use it when `lib.exe` is
    // absent and no archiver override is set. On a real MSVC host `lib.exe` is
    // found and used unchanged.
    let target = env::var("TARGET").unwrap_or_default();
    let target_underscored = target.replace('-', "_");
    let ar_overridden = env::var_os("AR").is_some()
        || env::var_os("TARGET_AR").is_some()
        || env::var_os(format!("AR_{target_underscored}")).is_some();
    if target.contains("-windows-msvc")
        && !ar_overridden
        && !find_on_path("lib.exe")
        && find_on_path("llvm-lib")
    {
        build.archiver("llvm-lib");
    }

    if arch.is_x86 {
        build.flag_if_supported("-mno-red-zone");
    }
    if arch.is_aarch64 && coff {
        build.flag("-march=armv8-a+nosimd");
    }

    build
        .define("_LIBC", None)
        .define("_FILE_OFFSET_BITS", "64")
        .define("DEFINE_MEMALIGN", "1")
        .define("DEFINE_POSIX_MEMALIGN", "1");

    // picolibc.h is the generated one in OUT_DIR/include — must come first.
    build.include(out_include_dir);
    for dir in &arch.include_dirs {
        build.include(picolibc_dir.join(dir));
    }
    build
        .include(picolibc_dir)
        .include(picolibc_dir.join("libc/stdio"))
        .include(picolibc_dir.join("libc/locale"))
        .include(picolibc_dir.join("libc/include"));

    Ok(build)
}

/// Source-level edits applied to picolibc libc sources on COFF/PE targets, keyed
/// by the libc-relative path. Active only when the `coff` feature is enabled.
///
/// picolibc ties stdin/stdout/stderr together with weak symbols, which the
/// COFF/clang toolchain mishandles. posixiob_*.c export them as weak *aliases*
/// via `__weak_reference` (which expands to a Mach-O `.weak_reference` directive
/// → a broken weak external), while fread.c / bufio.c declare them as weak
/// *references* (`__weak`) so they can be absent — on COFF those become weak
/// externals with per-TU default targets that collide. Together the COFF linker
/// reports stdin/stdout/stderr as duplicate symbols.
///
/// The edits below turn the alias strong (`__strong_reference`,
/// `__attribute__((alias))`) and drop the `__weak` qualifier from the
/// references, yielding a single strong definition that everything else binds
/// to. Note: this makes stdin/stdout/stderr *required* at link time (provide
/// them via `posix-console` or your own definitions).
fn coff_patches(rel: &str) -> &'static [(&'static str, &'static str)] {
    if !cfg!(feature = "coff") {
        return &[];
    }
    match rel {
        "stdio/posixiob_stdin.c" | "stdio/posixiob_stdout.c" | "stdio/posixiob_stderr.c" => {
            &[("__weak_reference", "__strong_reference")]
        }
        "stdio/fread.c" | "stdio/bufio.c" => &[
            ("const stdin __weak", "const stdin"),
            ("const stdout __weak", "const stdout"),
            ("const stderr __weak", "const stderr"),
        ],
        _ => &[],
    }
}

/// Resolve the on-disk source to compile for a libc-relative path. If the file
/// has COFF patches (and the `coff` feature is on), a patched copy is written
/// under `OUT_DIR/coff_patched/` and that path is returned; otherwise the
/// original vendored source is used.
fn libc_source(picolibc_dir: &Path, out_dir: &Path, rel: &str) -> Result<PathBuf> {
    let original = picolibc_dir.join("libc").join(rel);
    let patches = coff_patches(rel);
    if patches.is_empty() {
        return Ok(original);
    }
    let mut contents =
        fs::read_to_string(&original).with_context(|| format!("could not read {original:?}"))?;
    for (from, to) in patches {
        contents = contents.replace(from, to);
    }
    let dir = out_dir.join("coff_patched");
    fs::create_dir_all(&dir).with_context(|| format!("could not create {dir:?}"))?;
    let dst = dir.join(rel.replace('/', "_"));
    fs::write(&dst, contents).with_context(|| format!("could not write {dst:?}"))?;
    Ok(dst)
}

fn add_libc(
    build: &mut cc::Build,
    picolibc_dir: &Path,
    out_dir: &Path,
    arch: &ArchConfig,
    coff: bool,
) -> Result<()> {
    for file in LIBC_FILES.iter().chain(arch.libc_machine.iter()) {
        if arch.libc_generic_exclude.contains(file) {
            continue;
        }
        if coff && arch.coff_excluded_libc.contains(file) {
            continue;
        }
        build.file(libc_source(picolibc_dir, out_dir, file)?);
    }
    Ok(())
}

/// Add the POSIX-fd-backed stdin/stdout/stderr sources (`posixiob_*.c`), gated
/// on the `posix-console` feature. COFF patching is handled by [`libc_source`].
#[cfg(feature = "posix-console")]
fn add_posix_console(build: &mut cc::Build, picolibc_dir: &Path, out_dir: &Path) -> Result<()> {
    for file in LIBC_FILES_POSIX_CONSOLE {
        build.file(libc_source(picolibc_dir, out_dir, file)?);
    }
    Ok(())
}

#[cfg(not(feature = "posix-console"))]
fn add_posix_console(_: &mut cc::Build, _: &Path, _: &Path) -> Result<()> {
    Ok(())
}

fn add_libm(build: &mut cc::Build, picolibc_dir: &Path, arch: &ArchConfig) {
    build.include(picolibc_dir.join("libm/common"));
    for file in LIBM_FILES.iter().chain(arch.libm_machine.iter()) {
        build.file(picolibc_dir.join("libm").join(file));
    }
}

