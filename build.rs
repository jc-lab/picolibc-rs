//! Build script for the `picolibc` crate.
//!
//! Compiles picolibc-bsd from source with cc-rs, statically links the result,
//! generates `OUT_DIR/include/picolibc.h` reflecting the active cargo features,
//! optionally runs bindgen, and exports the include directory to dependents.

mod build_files;

use std::path::{Path, PathBuf};
use std::process::Command;
use std::{env, fs};

use anyhow::{bail, Context, Result};
#[cfg(feature = "posix-console")]
use build_files::LIBC_FILES_POSIX_CONSOLE;
use build_files::{
    LIBC_FILES, LIBC_FILES_AARCH64_ASM, LIBC_FILES_AARCH64_C, LIBC_FILES_AARCH64_EXCLUDE,
    LIBC_FILES_X86, LIBM_FILES, LIBM_FILES_X86,
};

// ---------------------------------------------------------------------------
// picolibc.h generation
// ---------------------------------------------------------------------------

/// Write `$OUT_DIR/include/picolibc.h` with the exact macro set that matches
/// the active cargo features. This replaces the former static `include/picolibc.h`.
///
/// Upstream picolibc normally generates this file from meson options; we do the
/// same thing here, driven by cargo feature flags.
fn generate_picolibc_h(include_dir: &Path) -> Result<()> {
    // Helper: either `#define NAME` or `#undef NAME` based on a boolean.
    fn define(h: &mut String, name: &str, enable: bool) {
        if enable {
            h.push_str(&format!("#define {name}\n"));
        } else {
            h.push_str(&format!("#undef  {name}\n"));
        }
    }

    let mut h = String::new();

    h.push_str("/* Auto-generated by build.rs — do not edit by hand.\n");
    h.push_str(" * Re-run `cargo build` after changing cargo features.\n");
    h.push_str(" * Corresponds to upstream picolibc's meson-generated picolibc.h.\n");
    h.push_str(" */\n\n");
    h.push_str("#pragma once\n\n");

    // ── version ──────────────────────────────────────────────────────────────
    h.push_str("/* version */\n");
    h.push_str("#define _NEWLIB_VERSION \"4.3.0\"\n");
    h.push_str("#define __NEWLIB_VERSION__ \"4.3.0\"\n");
    h.push_str("#define __NEWLIB__ 4\n");
    h.push_str("#define __NEWLIB_MINOR__ 3\n");
    h.push_str("#define __NEWLIB_PATCHLEVEL__ 0\n");
    h.push_str("#define __PICOLIBC_VERSION__ \"1.8.11\"\n");
    h.push_str("#define __PICOLIBC__ 1\n");
    h.push_str("#define __PICOLIBC_MINOR__ 8\n");
    h.push_str("#define __PICOLIBC_PATCHLEVEL__ 11\n\n");

    // ── always-on (freestanding/baremetal invariants) ─────────────────────────
    h.push_str("/* freestanding invariants — not user-configurable */\n");
    // single-thread: we target baremetal with no RTOS threading model
    h.push_str("#define __SINGLE_THREAD\n");
    // global errno: no TLS, single thread
    h.push_str("#define __GLOBAL_ERRNO\n");
    // initfini-array: all modern baremetal toolchains support this
    h.push_str("#define __INIT_FINI_ARRAY\n");
    // tiny stdio: only stdio picolibc ships since 1.8
    h.push_str("#define __TINY_STDIO\n");
    // format-default=double ('d')
    h.push_str("#define __IO_DEFAULT 'd'\n");
    // avoid software division in decimal conversion (always a win on baremetal)
    h.push_str("#define __IO_SMALL_ULTOA\n");
    // semihosting: off — use POSIX read/write stubs or posix-console feature
    h.push_str("#undef  __ARM_SEMIHOST\n");
    h.push_str("#undef  __SEMIHOST\n");
    // TLS: off — no runtime support on baremetal
    h.push_str("#undef  __THREAD_LOCAL_STORAGE\n");
    h.push_str("#undef  __THREAD_LOCAL_STORAGE_API\n");
    h.push_str("#undef  __THREAD_LOCAL_STORAGE_RP2040\n");
    h.push_str("#undef  __THREAD_LOCAL_STORAGE_STACK_GUARD\n");
    // picolibc's own malloc: off — embedder provides malloc (or use `malloc` feature)
    h.push_str("#undef  __ENABLE_MALLOC\n");
    h.push_str("#undef  __MALLOC_CLEAR_FREED\n");
    // fcntl: off — freestanding
    h.push_str("#undef  __HAVE_FCNTL\n");
    // stdio locking: off — single thread
    h.push_str("#undef  __STDIO_LOCKING\n");
    // atomic ungetc: off — single thread
    h.push_str("#undef  __ATOMIC_UNGETC\n");
    // obsolete math: off
    h.push_str("#undef  __OBSOLETE_MATH\n");
    h.push_str("#undef  __OBSOLETE_MATH_DOUBLE\n");
    h.push_str("#undef  __OBSOLETE_MATH_FLOAT\n");
    // size-over-speed: off — release build
    h.push_str("#undef  __PREFER_SIZE_OVER_SPEED\n");
    // legacy init/fini functions: off (use INIT_FINI_ARRAY instead)
    h.push_str("#undef  __INIT_FINI_FUNCS\n");
    h.push_str("#undef  __IEEEFP_FUNCS\n");
    h.push_str("#undef  __HAVE_BITFIELDS_IN_PACKED_STRUCTS\n");
    h.push('\n');

    // ── cargo-feature-controlled options ─────────────────────────────────────
    h.push_str("/* cargo-feature-controlled options */\n");

    // assert-verbose: emit file/line info in assert() failures
    define(&mut h, "__ASSERT_VERBOSE", cfg!(feature = "assert-verbose"));

    // fast-strcmp: optimise strcmp for speed (vs size)
    define(&mut h, "__FAST_STRCMP", cfg!(feature = "fast-strcmp"));

    // fast-bufio: faster buffered I/O operations
    define(&mut h, "__FAST_BUFIO", cfg!(feature = "fast-bufio"));

    // io-float-exact: round-trip accurate float/string conversion
    define(&mut h, "__IO_FLOAT_EXACT", cfg!(feature = "io-float-exact"));

    // io-wchar: wide-character support in printf/scanf
    define(&mut h, "__IO_WCHAR", cfg!(feature = "io-wchar"));

    // io-c99-formats: %zu, %hhu, %td, %jd, etc.
    define(&mut h, "__IO_C99_FORMATS", cfg!(feature = "io-c99-formats"));

    // io-long-long: %lld/%llu/%llo/%llx in printf/scanf
    define(&mut h, "__IO_LONG_LONG", cfg!(feature = "io-long-long"));

    // io-long-double: %Lf/%Le/%Lg in printf/scanf
    define(&mut h, "__IO_LONG_DOUBLE", cfg!(feature = "io-long-double"));

    // io-pos-args: positional arguments (%1$d) in printf
    define(&mut h, "__IO_POS_ARGS", cfg!(feature = "io-pos-args"));

    // io-percent-b: %b/%B binary integer format (C23)
    define(&mut h, "__IO_PERCENT_B", cfg!(feature = "io-percent-b"));

    // printf-percent-n: %n in printf (off by default for security)
    define(&mut h, "__IO_PERCENT_N", cfg!(feature = "printf-percent-n"));

    // minimal-io-long-long: %lld in the minimal printf/scanf variant
    define(
        &mut h,
        "__IO_MINIMAL_LONG_LONG",
        cfg!(feature = "minimal-io-long-long"),
    );

    // mb-capable: multibyte / UTF-8 charset support
    define(&mut h, "__MB_CAPABLE", cfg!(feature = "mb-capable"));

    // math-errno: set errno in math functions per C standard.
    // When off, IEEE mode (__IEEE_LIBM) is used — no errno side-effects.
    let math_errno = cfg!(feature = "math-errno");
    define(&mut h, "__MATH_ERRNO", math_errno);
    define(&mut h, "__IEEE_LIBM", !math_errno);
    h.push('\n');

    // ── compiler feature detection (unchanged from upstream) ─────────────────
    h.push_str("/* compiler built-in feature detection */\n");
    h.push_str(
        r#"
#ifndef __has_builtin
#define picolibc_has_builtin(x) 0
#else
#define picolibc_has_builtin(x) __has_builtin(x)
#endif

#if picolibc_has_builtin(__builtin_alloca)
#define __HAVE_BUILTIN_ALLOCA 1
#endif
#if picolibc_has_builtin(__builtin_ffs)
#define __HAVE_BUILTIN_FFS 1
#endif
#if picolibc_has_builtin(__builtin_ffsl)
#define __HAVE_BUILTIN_FFSL 1
#endif
#if picolibc_has_builtin(__builtin_ffsll)
#define __HAVE_BUILTIN_FFSLL 1
#endif
#if picolibc_has_builtin(__builtin_ctz)
#define __HAVE_BUILTIN_CTZ 1
#endif
#if picolibc_has_builtin(__builtin_ctzl)
#define __HAVE_BUILTIN_CTZL 1
#endif
#if picolibc_has_builtin(__builtin_ctzll)
#define __HAVE_BUILTIN_CTZLL 1
#endif
#if picolibc_has_builtin(__builtin_copysign)
#define __HAVE_BUILTIN_COPYSIGN 1
#endif
#if picolibc_has_builtin(__builtin_copysignl)
#define __HAVE_BUILTIN_COPYSIGNL 1
#endif
#if picolibc_has_builtin(__builtin_isinf)
#define __HAVE_BUILTIN_ISINF 1
#endif
#if picolibc_has_builtin(__builtin_isinfl)
#define __HAVE_BUILTIN_ISINFL 1
#endif
#if picolibc_has_builtin(__builtin_isnan)
#define __HAVE_BUILTIN_ISNAN 1
#endif
#if picolibc_has_builtin(__builtin_isnanl)
#define __HAVE_BUILTIN_ISNANL 1
#endif
#if picolibc_has_builtin(__builtin_isfinite)
#define __HAVE_BUILTIN_ISFINITE 1
#endif
#if picolibc_has_builtin(__builtin_finitel)
#define __HAVE_BUILTIN_FINITEL 1
#endif
#if picolibc_has_builtin(__builtin_issignalingl)
#define __HAVE_BUILTIN_ISSIGNALINGL 1
#endif
#if picolibc_has_builtin(__builtin_expect)
#define __HAVE_BUILTIN_EXPECT 1
#endif
#if picolibc_has_builtin(__builtin_complex)
#define __HAVE_BUILTIN_COMPLEX 1
#endif
#if picolibc_has_builtin(__builtin_add_overflow)
#define __HAVE_BUILTIN_ADD_OVERFLOW 1
#endif
#if picolibc_has_builtin(__builtin_mul_overflow)
#define __HAVE_BUILTIN_MUL_OVERFLOW 1
#endif

#if !defined(__STDC_NO_COMPLEX__)
#define __HAVE_COMPLEX 1
#endif

#undef picolibc_has_builtin
"#,
    );

    fs::write(include_dir.join("picolibc.h"), &h)
        .context("could not write generated picolibc.h")?;
    Ok(())
}

// ---------------------------------------------------------------------------
// Architecture configuration
// ---------------------------------------------------------------------------

struct ArchConfig {
    libc_machine: Vec<&'static str>,
    libm_machine: Vec<&'static str>,
    /// Generic `LIBC_FILES` entries replaced by machine sources (drop to avoid
    /// duplicate symbols).
    libc_generic_exclude: Vec<&'static str>,
    /// Machine libc sources that must be dropped on COFF/Win64 (ELF asm).
    coff_excluded_libc: Vec<&'static str>,
    include_dirs: Vec<&'static str>,
    /// `libc/machine/<arch>/machine/` directory, relative to the picolibc root.
    ///
    /// The arch-specific `machine/*.h` headers (e.g. `machine/fenv.h` with the
    /// struct-based `fenv_t` for x86) must shadow the generic
    /// `libc/include/machine/` headers in `OUT_DIR/include/machine/`. Without
    /// this override the generic `typedef int fenv_t` is found first, causing
    /// the x86 `libm/machine/x86/fenv.c` to fail to compile.
    machine_headers_dir: Option<&'static str>,
    is_x86: bool,
    is_aarch64: bool,
}

fn arch_config(target_arch: &str) -> Result<ArchConfig> {
    match target_arch {
        "x86" | "x86_64" => Ok(ArchConfig {
            libc_machine: LIBC_FILES_X86.to_vec(),
            libm_machine: LIBM_FILES_X86.to_vec(),
            libc_generic_exclude: Vec::new(),
            coff_excluded_libc: vec![
                "machine/x86/interrupt.S",
                "machine/x86/setjmp.S",
                "machine/x86/tcb.S",
            ],
            include_dirs: vec!["libc/machine/x86", "libm/machine/x86"],
            machine_headers_dir: Some("libc/machine/x86/machine"),
            is_x86: true,
            is_aarch64: false,
        }),
        "aarch64" => Ok(ArchConfig {
            libc_machine: LIBC_FILES_AARCH64_C
                .iter()
                .chain(LIBC_FILES_AARCH64_ASM.iter())
                .copied()
                .collect(),
            libm_machine: Vec::new(),
            libc_generic_exclude: LIBC_FILES_AARCH64_EXCLUDE.to_vec(),
            coff_excluded_libc: LIBC_FILES_AARCH64_ASM.to_vec(),
            include_dirs: vec!["libc/machine/aarch64"],
            machine_headers_dir: Some("libc/machine/aarch64/machine"),
            is_x86: false,
            is_aarch64: true,
        }),
        arch => bail!(
            "picolibc: unsupported target architecture {arch:?}. \
             Only x86/x86_64 and aarch64 are wired up so far; see arch_config() \
             in build.rs to add another architecture."
        ),
    }
}

